#OPERADORES DE ATRIBUIÇÃO
#ATRIBUIÇÃO SIMPLES
saldo = 500
print(saldo)
#ATRIBUIÇÃO COM ADIÇÃO
saldo += 200
print(saldo)
#ATRIBUIÇÃO COM SUBTRAÇÃO
saldo -= 200
print(saldo)
#ATRIBUIÇÃO COM MULTIPLICAÇÃO
saldo *= 200
print(saldo)
#ATRIBUIÇÃO COM DIVISÃO
saldo /= 5
print(saldo)
#ATRIBUIÇÃO COM DIVISÃO INTEIRA
saldo //= 5
print(saldo)
#ATRIBUIÇÃO COM MODULO
saldo %= 480
print(saldo)
#ATRIBUIÇÃO COM EXPONENCIAÇÃO
saldo **= 2
print(saldo)


